class FeuilleContact {
  String? name, phoneNumber, mail;

  FeuilleContact({this.name, this.phoneNumber, this.mail});

  setName(String name) {
    this.name = name;
  }

  setPhoneNumber(String phoneNumber) {
    this.phoneNumber = phoneNumber;
  }

  setmail(String mail) {
    this.mail = mail;
  }
}
